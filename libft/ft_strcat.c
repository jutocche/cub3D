#include "libft.h"

char	*ft_strcat(char *s2, char *s1)
{
	int	c;
	int	d;

	c = 0;
	d = 0;
	while (s2[c] != '\0')
		c++;
	while (s1[d] != '\0')
	{
		s2[c] = s1[d];
		c++;
		d++;
	}
	s2[c] = '\0';
	return (s2);
}
